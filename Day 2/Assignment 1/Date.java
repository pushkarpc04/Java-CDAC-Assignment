class Date {
private int day, month, year;


public int getday(){
	return day;
}

public void setday(int day){
	this.day = day;
}

public int getmonth(){
	return month;
}
public void setmonth(int month){
	this.month = month;
}
public int getyear(){
	return year;
}
public void setyear(int year){
	this.year = year;
}

}